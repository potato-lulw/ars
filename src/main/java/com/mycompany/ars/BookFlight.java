    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(url, user, password);

            String source = (String) sourceBox.getSelectedItem();
            String destination = (String) destinationBox.getSelectedItem();

            String query = "SELECT FlightID, FlightName, Arrival, Departure, Date, Fare FROM flight WHERE Source = ? AND Destination = ?";
            pre = con.prepareStatement(query);

            pre.setString(1, source);
            pre.setString(2, destination);

            ResultSet rs = pre.executeQuery();

            // Get table model from JTable
            DefaultTableModel model = (DefaultTableModel) tableModel.getModel();

            // Clear previous data from the table
            model.setRowCount(0);

            // Add rows to the table model
            while (rs.next()) {
                String flightID = rs.getString("FlightID");
                String flightName = rs.getString("FlightName");
                String arrival = rs.getString("Arrival");
                String departure = rs.getString("Departure");
                String date = rs.getString("Date");
                int fare = rs.getInt("Fare");

                // Add row to the table model
                model.addRow(new Object[]{flightID, flightName, arrival, departure, date, fare});
            }

            // Close resources
            rs.close();
            pre.close();
            con.close();

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

