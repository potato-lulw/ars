    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String FliID = fID.getText();
        String fare = totalFare.getText();
        String firstName = fName.getText();
        String lastName = lName.getText();
        String tickID = ticketID.getText();
        String nooftick = noOfTickets.getText();
        String contact = conn.getText();

        try {
            // Establish database connection
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(url, user, password);

            // SQL query to insert ticket details
            String query = "INSERT INTO ticket (TicketID, FirstName, LastName, FlightID, NoOfTickets, TotalFare, Contact) VALUES (?, ?, ?, ?, ?, ?, ?)";
            pre = con.prepareStatement(query);

            // Set values in the query
            pre.setString(1, tickID);
            pre.setString(2, firstName);
            pre.setString(3, lastName);
            pre.setString(4, FliID);
            pre.setString(5, nooftick);
            pre.setString(6, fare);
            pre.setString(7, contact);

            // Execute the query
            int rowsInserted = pre.executeUpdate();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Ticket booked successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Clear input fields after successful booking
                fID.setText("");
                totalFare.setText("");
                fName.setText("");
                lName.setText("");
                ticketID.setText("");
                noOfTickets.setText("");
                conn.setText("");
                fa.setText("");
                
                
            } else {
                JOptionPane.showMessageDialog(this, "Booking failed!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Close resources
            pre.close();
            con.close();

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "An error occurred while booking!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

